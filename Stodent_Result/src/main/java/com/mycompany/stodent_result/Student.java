    private void btnRankingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRankingActionPerformed
        double [] R = new double [14];
        
        R[0] = Double.parseDouble(txtMaths.getText());
        R[1] = Double.parseDouble(txtEnglish.getText());
        R[2] = Double.parseDouble(txtBiology.getText());
        R[3] = Double.parseDouble(txtComputer.getText());
        R[4] = Double.parseDouble(txtChemistry.getText());
        R[5] = Double.parseDouble(txtPhysics.getText());
        R[6] = Double.parseDouble(txtAddMaths.getText());
        R[7] = Double.parseDouble(txtBusiness.getText());
        
        R[8] = (R[0] + R[1] + R[2] + R[3] + R[4] + R[5] + R[6] + R[7]) / 8;
        R[9] = R[0] + R[1] + R[2] + R[3] + R[4] + R[5] + R[6] + R[7];
        
        String Average = String.format("%.0f",R[8]);
        txtAverage.setText(Average);
        
        String TotalScore = String.format("%.0f",R[9]);
        txtTotalScore.setText(TotalScore);
        
        if(R[9] >= 700)
        {
            txtRanking.setText("1st");
        }
        else if (R[9] >= 600)
        {
            txtRanking.setText("2nd");
        }
        else if (R[9] >= 500)
        {
            txtRanking.setText("3d");
        }
        else if (R[9] >= 400)
        {
            txtRanking.setText("4th");
        }
        else if (R[9] >= 200)
        {
            txtRanking.setText("Fail");
        }
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{
            txtStudentID.getText(),
            cbCourseCode.getSelectedItem(),
            txtMaths.getText(),
            txtEnglish.getText(),
            txtBiology.getText(),
            txtComputer.getText(),
            txtChemistry.getText(),
            txtPhysics.getText(),
            txtAddMaths.getText(),
            txtBusiness.getText(),
            txtTotalScore.getText(),
            txtAverage.getText(),
            txtRanking.getText(),
        });
    }//GEN-LAST:event_btnRankingActionPerformed

